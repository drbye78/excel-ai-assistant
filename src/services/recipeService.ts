/**
 * Excel AI Assistant - Recipe Service
 * Templates & Recipes system for reusable Excel workflows
 * 
 * @module services/recipeService
 */

import { logger } from '../utils/logger';

// ============================================================================
// Type Definitions
// ============================================================================

/** A recipe is a reusable template for Excel operations */
export interface Recipe {
  id: string;
  name: string;
  description: string;
  category: RecipeCategory;
  tags: string[];
  prompt: string;
  expectedInputs: RecipeInput[];
  previewImage?: string;
  author: string;
  authorId?: string;
  rating: number;
  ratingCount: number;
  usageCount: number;
  createdAt: Date;
  updatedAt: Date;
  version: string;
  isBuiltIn: boolean;
  isPublic: boolean;
  difficulty: "beginner" | "intermediate" | "advanced";
  estimatedTime?: string;
}

/** Recipe categories for organization */
export type RecipeCategory =
  | "data-cleaning"
  | "reporting"
  | "analysis"
  | "formatting"
  | "financial"
  | "dashboard"
  | "import-export"
  | "automation"
  | "custom";

/** Input parameter for a recipe */
export interface RecipeInput {
  name: string;
  type: "text" | "number" | "range" | "column" | "table" | "boolean" | "select" | "multi-select";
  required: boolean;
  label: string;
  description?: string;
  defaultValue?: any;
  options?: string[]; // For select/multi-select types
  validation?: InputValidation;
}

/** Input validation rules */
export interface InputValidation {
  min?: number;
  max?: number;
  pattern?: string;
  message?: string;
}

/** Filters for recipe search */
export interface RecipeFilters {
  category?: RecipeCategory;
  tags?: string[];
  search?: string;
  author?: string;
  difficulty?: "beginner" | "intermediate" | "advanced";
  minRating?: number;
  isBuiltIn?: boolean;
  sortBy?: "popular" | "newest" | "rating" | "name";
}

/** Result of executing a recipe */
export interface RecipeExecutionResult {
  success: boolean;
  message: string;
  actions?: RecipeAction[];
  error?: string;
  executionTime?: number;
}

/** Actions generated by recipe execution */
export interface RecipeAction {
  type: "insert_formula" | "set_values" | "create_chart" | "create_table" | "format_cells" | "add_validation" | "apply_conditional_formatting";
  description: string;
  target?: string;
  payload: any;
}

/** User's saved recipe with customizations */
export interface UserRecipe extends Recipe {
  originalRecipeId?: string;
  customizations?: Record<string, any>;
  lastUsed?: Date;
  useCount: number;
}

/** Recipe rating submitted by user */
export interface RecipeRating {
  recipeId: string;
  userId: string;
  rating: number;
  comment?: string;
  createdAt: Date;
}

/** Recipe execution history */
export interface RecipeExecution {
  recipeId: string;
  recipeName: string;
  executedAt: Date;
  inputs: Record<string, any>;
  result: RecipeExecutionResult;
  workbookName?: string;
}

