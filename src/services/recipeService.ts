  /**
   * Rate a recipe
   */
  rateRecipe(recipeId: string, rating: number, comment?: string): boolean {
    if (rating < 1 || rating > 5) return false;

    const recipe = this.recipes.get(recipeId);
    if (recipe) {
      // Update average rating
      const totalRating = recipe.rating * recipe.ratingCount + rating;
      recipe.ratingCount++;
      recipe.rating = totalRating / recipe.ratingCount;
      return true;
    }

    const userRecipe = this.userRecipes.get(recipeId);
    if (userRecipe) {
      const totalRating = userRecipe.rating * userRecipe.ratingCount + rating;
      userRecipe.ratingCount++;
      userRecipe.rating = totalRating / userRecipe.ratingCount;
      this.saveUserRecipes();
      return true;
    }

    return false;
  }

